public class IncrementEx2 {

    public static void main(String[] args) {

        int cow = 0;
        System.out.println( cow++ );    // 0
        System.out.println( cow );      // 1

        int pig = 4;
        pig = pig++; // returns the original value. The pig variable is then assigned THE ORIGNAL VALUE value
        System.out.println(pig); // 4




        
    }
    
}
